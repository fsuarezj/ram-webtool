package com.lrc.liferay.ram.utils.flowcharts.exceptions;

public class ModifiedFlowException extends Throwable {

	public ModifiedFlowException() {
		super();
	}

	public ModifiedFlowException(String msg) {
		super(msg);
	}

	public ModifiedFlowException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public ModifiedFlowException(Throwable cause) {
		super(cause);
	}
}
