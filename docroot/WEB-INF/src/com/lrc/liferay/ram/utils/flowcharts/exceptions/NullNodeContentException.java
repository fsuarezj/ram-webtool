package com.lrc.liferay.ram.utils.flowcharts.exceptions;

public class NullNodeContentException extends Throwable {

	public NullNodeContentException() {
		super();
	}

	public NullNodeContentException(String msg) {
		super(msg);
	}

	public NullNodeContentException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public NullNodeContentException(Throwable cause) {
		super(cause);
	}
}
