package com.lrc.liferay.ram.utils.flowcharts.exceptions;

public class NonExistingNodeException extends Throwable {

	public NonExistingNodeException() {
		super();
	}

	public NonExistingNodeException(String msg) {
		super(msg);
	}

	public NonExistingNodeException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public NonExistingNodeException(Throwable cause) {
		super(cause);
	}
}
