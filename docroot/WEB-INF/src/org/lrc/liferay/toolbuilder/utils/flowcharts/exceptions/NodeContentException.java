package org.lrc.liferay.toolbuilder.utils.flowcharts.exceptions;

public class NodeContentException extends Throwable {

	public NodeContentException() {
		super();
	}

	public NodeContentException(String msg) {
		super(msg);
	}

	public NodeContentException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public NodeContentException(Throwable cause) {
		super(cause);
	}
}
